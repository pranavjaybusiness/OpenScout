const OPENSCOUT_DOMAIN_SET = new Set(
    OPENSCOUT_SUPPORTED_ROOT_DOMAINS.filter(Boolean).map((d) => d.toLowerCase())
);

/** @param {string} hostname */
function normalizeHostname(hostname) {
    return (hostname || "").toLowerCase().replace(/^www\./, "");
}

class SupportedSites {
    static isAllowedHostname(hostname) {
        const host = normalizeHostname(hostname);
        if (!host || host === "ebay.com" || host.endsWith(".ebay.com")) {
            return false;
        }
        for (const root of OPENSCOUT_DOMAIN_SET) {
            if (host === root || host.endsWith(`.${root}`)) {
                return true;
            }
        }
        return false;
    }

    static isCurrentSiteSupported() {
        return SupportedSites.isAllowedHostname(window.location.hostname);
    }

    /** Chrome content_script / host_permission match patterns */
    static manifestMatchPatterns() {
        const patterns = [];
        for (const root of OPENSCOUT_DOMAIN_SET) {
            patterns.push(`*://*.${root}/*`, `*://${root}/*`);
        }
        return patterns;
    }
}
